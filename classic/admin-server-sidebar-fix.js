app.get('/api/sidebar-config', authenticate, async (req, res) => {
  try {
    const sidebarsPath = path.join(__dirname, 'sidebars.ts');
    const content = await fs.readFile(sidebarsPath, 'utf-8');
    
    // Parse the refinedSidebars.tutorialSidebar array
    const sidebarMatch = content.match(/const refinedSidebars[^=]*=\s*\{[\s\S]*?tutorialSidebar:\s*\[([\s\S]*?)\]\s*\};/s);
    
    if (!sidebarMatch) {
      return res.status(500).json({ error: 'Could not parse sidebars.ts - refinedSidebars not found' });
    }

    const sidebarContent = sidebarMatch[1].trim();
    const items = [];
    
    // Helper to parse string values (handles both single and double quotes)
    const parseStringValue = (str) => {
      str = str.trim();
      if ((str.startsWith('"') && str.endsWith('"')) || (str.startsWith("'") && str.endsWith("'"))) {
        return str.slice(1, -1).replace(/\\(.)/g, '$1');
      }
      return str;
    };
    
    // Helper to parse a sidebar item object
    const parseSidebarItem = (itemStr) => {
      const item = {};
      
      // Extract type
      const typeMatch = itemStr.match(/type:\s*['"]([^'"]+)['"]/);
      if (typeMatch) {
        item.type = typeMatch[1];
      }
      
      // Extract label
      const labelMatch = itemStr.match(/label:\s*['"]([^'"]+)['"]/);
      if (labelMatch) {
        item.label = labelMatch[1];
      }
      
      // Extract href (for links)
      const hrefMatch = itemStr.match(/href:\s*['"]([^'"]+)['"]/);
      if (hrefMatch) {
        item.href = hrefMatch[1];
      }
      
      // Extract dirName (for autogenerated)
      const dirNameMatch = itemStr.match(/dirName:\s*['"]([^'"]+)['"]/);
      if (dirNameMatch) {
        item.dirName = dirNameMatch[1];
      }
      
      // Extract id (for docs)
      const idMatch = itemStr.match(/id:\s*['"]([^'"]+)['"]/);
      if (idMatch) {
        item.id = idMatch[1];
      }
      
      // Extract link object (for categories)
      const linkMatch = itemStr.match(/link:\s*\{\s*type:\s*['"]([^'"]+)['"]\s*\}/);
      if (linkMatch) {
        item.link = { type: linkMatch[1] };
      }
      
      // Extract collapsible
      const collapsibleMatch = itemStr.match(/collapsible:\s*(true|false)/);
      if (collapsibleMatch) {
        item.collapsible = collapsibleMatch[1] === 'true';
      }
      
      // Extract collapsed
      const collapsedMatch = itemStr.match(/collapsed:\s*(true|false)/);
      if (collapsedMatch) {
        item.collapsed = collapsedMatch[1] === 'true';
      }
      
      // Extract items array (nested items)
      const itemsMatch = itemStr.match(/items:\s*\[([^\]]+)\]/s);
      if (itemsMatch) {
        const itemsContent = itemsMatch[1];
        const nestedItems = [];
        
        // Find all nested objects
        let depth = 0;
        let start = -1;
        for (let i = 0; i < itemsContent.length; i++) {
          if (itemsContent[i] === '{') {
            if (depth === 0) start = i;
            depth++;
          } else if (itemsContent[i] === '}') {
            depth--;
            if (depth === 0 && start >= 0) {
              const nestedItemStr = itemsContent.substring(start, i + 1);
              const nestedItem = parseSidebarItem(nestedItemStr);
              if (nestedItem.type) {
                nestedItems.push(nestedItem);
              }
              start = -1;
            }
          }
        }
        
        if (nestedItems.length > 0) {
          item.items = nestedItems;
        }
      }
      
      // Generate ID if not present
      if (!item.id) {
        const idBase = item.label || item.dirName || item.href || `item-${items.length}`;
        item.id = `sidebar-${idBase.toLowerCase().replace(/[^a-z0-9]+/g, '-')}`;
      }
      
      return item;
    };
    
    // Find all top-level objects in the array
    let braceDepth = 0;
    let startPos = -1;
    let inString = false;
    let stringChar = '';
    
    for (let i = 0; i < sidebarContent.length; i++) {
      const char = sidebarContent[i];
      const prevChar = i > 0 ? sidebarContent[i - 1] : '';
      
      // Handle strings (skip braces inside strings)
      if (!inString && (char === '"' || char === "'")) {
        inString = true;
        stringChar = char;
      } else if (inString && char === stringChar && prevChar !== '\\') {
        inString = false;
        stringChar = '';
      }
      
      if (!inString) {
        if (char === '{') {
          if (braceDepth === 0) {
            startPos = i;
          }
          braceDepth++;
        } else if (char === '}') {
          braceDepth--;
          if (braceDepth === 0 && startPos >= 0) {
            const itemStr = sidebarContent.substring(startPos, i + 1);
            const item = parseSidebarItem(itemStr);
            if (item.type) {
              items.push(item);
            }
            startPos = -1;
          }
        }
      }
    }
    
    console.log(`Parsed ${items.length} sidebar items from sidebars.ts`);
    res.json(items);
  } catch (error) {
    console.error('Error parsing sidebar config:', error);
    res.status(500).json({ error: error.message || 'Failed to parse sidebar config' });
  }
});

