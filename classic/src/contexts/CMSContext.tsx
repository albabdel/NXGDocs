import React, { createContext, useContext, useState, useCallback, ReactNode } from 'react';

// Types
export type Resource = {
  id: string;
  title: string;
  description: string;
  link: string;
  icon: string; // Icon name from lucide-react
};

export type SidebarItem = {
  id: string;
  type: 'link' | 'category' | 'html' | 'doc' | 'autogenerated';
  label?: string;
  href?: string;
  items?: SidebarItem[];
  dirName?: string;
  value?: string;
  collapsible?: boolean;
  collapsed?: boolean;
  link?: {
    type: 'generated-index' | 'doc';
  };
};

export type ArticleContent = {
  docId: string;
  title: string;
  content: string; // HTML content from rich text editor
  frontmatter: Record<string, any>;
};

export type CMSConfig = {
  landingPage: {
    heroTitle: string;
    heroSubtitle: string;
    featuredHeading: string;
    featuredSubtitle: string;
    roleHeading: string;
    roleSubtitle: string;
    learnMoreHeading: string;
    learnMoreSubtitle: string;
    coreResources: Resource[];
    featuredResources: Resource[];
    roleResources: Resource[];
    learnMoreResources: Resource[];
  };
  sidebar: SidebarItem[];
  articles: Record<string, ArticleContent>;
};

interface CMSContextType {
  config: CMSConfig;
  isDirty: boolean;
  isEditMode: boolean;
  setEditMode: (enabled: boolean) => void;
  updateLandingPage: (section: keyof CMSConfig['landingPage'], resources: Resource[]) => void;
  updateHeroText: (title: string, subtitle: string) => void;
  updateSectionText: (section: 'featured' | 'role' | 'learnMore', heading: string, subtitle: string) => void;
  updateSidebar: (sidebar: SidebarItem[]) => void;
  updateArticle: (docId: string, content: ArticleContent) => void;
  addResource: (section: keyof CMSConfig['landingPage'], resource: Resource) => void;
  removeResource: (section: keyof CMSConfig['landingPage'], id: string) => void;
  addSidebarItem: (parentId: string | null, item: SidebarItem) => void;
  removeSidebarItem: (id: string) => void;
  renameSidebarItem: (id: string, newLabel: string) => void;
  moveSidebarItem: (itemId: string, newParentId: string | null) => void;
  exportConfig: () => string;
  loadConfig: (config: CMSConfig) => void;
  markDirty: () => void;
  markClean: () => void;
}

