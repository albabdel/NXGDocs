export function CMSProvider({ children }: { children: ReactNode }) {
  const [config, setConfig] = useState<CMSConfig>(() => {
    // Only try to load from localStorage on the client side
    if (typeof window === 'undefined') {
      return defaultConfig;
    }

    try {
      const saved = localStorage.getItem('cms_config');
      if (saved) {
        const parsed = JSON.parse(saved);
        // Merge with default to ensure all fields exist
        return {
          ...defaultConfig,
          ...parsed,
          landingPage: {
            ...defaultConfig.landingPage,
            ...parsed.landingPage,
          },
        };
      }
    } catch {
      return defaultConfig;
    }

    return defaultConfig;
  });
  const [isDirty, setIsDirty] = useState(false);
  const [isEditMode, setIsEditMode] = useState(false);

  const markDirty = useCallback(() => setIsDirty(true), []);
  const markClean = useCallback(() => setIsDirty(false), []);

  const updateLandingPage = useCallback(
    (section: keyof CMSConfig['landingPage'], resources: Resource[]) => {
      setConfig((prev) => ({
        ...prev,
        landingPage: {
          ...prev.landingPage,
          [section]: resources,
        },
      }));
      markDirty();
    },
    [markDirty]
  );

  const updateHeroText = useCallback(
    (title: string, subtitle: string) => {
      setConfig((prev) => ({
        ...prev,
        landingPage: {
          ...prev.landingPage,
          heroTitle: title,
          heroSubtitle: subtitle,
        },
      }));
      markDirty();
    },
    [markDirty]
  );

  const updateSectionText = useCallback(
    (section: 'featured' | 'role' | 'learnMore', heading: string, subtitle: string) => {
      setConfig((prev) => ({
        ...prev,
        landingPage: {
          ...prev.landingPage,
          [`${section}Heading`]: heading,
          [`${section}Subtitle`]: subtitle,
        },
      }));
      markDirty();
    },
    [markDirty]
  );

  const updateSidebar = useCallback(
    (sidebar: SidebarItem[]) => {
      setConfig((prev) => ({ ...prev, sidebar }));
      markDirty();
    },
    [markDirty]
  );

  const updateArticle = useCallback(
    (docId: string, content: ArticleContent) => {
      setConfig((prev) => ({
        ...prev,
        articles: {
          ...prev.articles,
          [docId]: content,
        },
      }));
      markDirty();
    },
    [markDirty]
  );

  const addResource = useCallback(
    (section: keyof CMSConfig['landingPage'], resource: Resource) => {
      setConfig((prev) => {
        const sectionData = prev.landingPage[section];
        if (Array.isArray(sectionData)) {
          return {
            ...prev,
            landingPage: {
              ...prev.landingPage,
              [section]: [...sectionData, resource],
            },
          };
        }
        return prev;
      });
      markDirty();
    },
    [markDirty]
  );

  const removeResource = useCallback(
    (section: keyof CMSConfig['landingPage'], id: string) => {
      setConfig((prev) => {
        const sectionData = prev.landingPage[section];
        if (Array.isArray(sectionData)) {
          return {
            ...prev,
            landingPage: {
              ...prev.landingPage,
              [section]: sectionData.filter((r) => r.id !== id),
            },
          };
        }
        return prev;
      });
      markDirty();
    },
    [markDirty]
  );

  const findSidebarItem = useCallback((items: SidebarItem[], id: string): SidebarItem | null => {
    for (const item of items) {
      if (item.id === id) return item;
      if (item.items) {
        const found = findSidebarItem(item.items, id);
        if (found) return found;
      }
    }
    return null;
  }, []);

  const removeSidebarItemRecursive = useCallback(
    (items: SidebarItem[], id: string): SidebarItem[] => {
      return items
        .filter((item) => item.id !== id)
        .map((item) => ({
          ...item,
          items: item.items ? removeSidebarItemRecursive(item.items, id) : undefined,
        }));
    },
    []
  );

  const removeSidebarItem = useCallback(
    (id: string) => {
      setConfig((prev) => ({
        ...prev,
        sidebar: removeSidebarItemRecursive(prev.sidebar, id),
      }));
      markDirty();
    },
    [markDirty, removeSidebarItemRecursive]
  );

  const renameSidebarItem = useCallback(
    (id: string, newLabel: string) => {
      const renameRecursive = (items: SidebarItem[]): SidebarItem[] => {
        return items.map((item) => ({
          ...item,
          label: item.id === id ? newLabel : item.label,
          items: item.items ? renameRecursive(item.items) : undefined,
        }));
      };

      setConfig((prev) => ({
        ...prev,
        sidebar: renameRecursive(prev.sidebar),
      }));
      markDirty();
    },
    [markDirty]
  );

  const addSidebarItem = useCallback(
    (parentId: string | null, item: SidebarItem) => {
      if (!parentId) {
        setConfig((prev) => ({
          ...prev,
          sidebar: [...prev.sidebar, item],
        }));
      } else {
        const addRecursive = (items: SidebarItem[]): SidebarItem[] => {
          return items.map((i) => {
            if (i.id === parentId) {
              return {
                ...i,
                items: [...(i.items || []), item],
              };
            }
            return {
              ...i,
              items: i.items ? addRecursive(i.items) : undefined,
            };
          });
        };

        setConfig((prev) => ({
          ...prev,
          sidebar: addRecursive(prev.sidebar),
        }));
      }
      markDirty();
    },
    [markDirty]
  );

  const moveSidebarItem = useCallback(
    (itemId: string, newParentId: string | null) => {
      let itemToMove: SidebarItem | null = null;
      
      const removeItem = (items: SidebarItem[]): SidebarItem[] => {
        return items.filter(item => {
          if (item.id === itemId) {
            itemToMove = item;
            return false;
          }
          return {
            ...item,
            items: item.items ? removeItem(item.items) : undefined,
          };
        }).map(item => ({
          ...item,
          items: item.items ? removeItem(item.items) : undefined,
        }));
      };

      const insertItem = (items: SidebarItem[]): SidebarItem[] => {
        if (newParentId === null) {
          return [...items, itemToMove!];
        }
        return items.map(item => {
          if (item.id === newParentId) {
            return {
              ...item,
              items: [...(item.items || []), itemToMove!],
            };
          }
          return {
            ...item,
            items: item.items ? insertItem(item.items) : undefined,
          };
        });
      };

      setConfig(prev => {
        const withoutItem = removeItem(prev.sidebar);
        return {
          ...prev,
          sidebar: itemToMove ? insertItem(withoutItem) : prev.sidebar,
        };
      });
      markDirty();
    },
    [markDirty]
  );

  const exportConfig = useCallback(() => {
    return JSON.stringify(config, null, 2);
  }, [config]);

  const loadConfig = useCallback((newConfig: CMSConfig) => {
    setConfig(newConfig);
    if (typeof window !== 'undefined') {
      localStorage.setItem('cms_config', JSON.stringify(newConfig));
    }
    markClean();
  }, [markClean]);

  // Auto-save to localStorage
  React.useEffect(() => {
    if (isDirty && typeof window !== 'undefined') {
      localStorage.setItem('cms_config', JSON.stringify(config));
    }
  }, [config, isDirty]);

  return (
    <CMSContext.Provider
      value={{
        config,
        isDirty,
        isEditMode,
        setEditMode: setIsEditMode,
        updateLandingPage,
        updateHeroText,
        updateSectionText,
        updateSidebar,
        updateArticle,
        addResource,
        removeResource,
        addSidebarItem,
        removeSidebarItem,
        renameSidebarItem,
        moveSidebarItem,
        exportConfig,
        loadConfig,
        markDirty,
        markClean,
      }}
    >
      {children}
    </CMSContext.Provider>
  );
}

export function useCMS() {
  const context = useContext(CMSContext);
  if (!context) {
    throw new Error('useCMS must be used within CMSProvider');
  }
  return context;
}

